package Spotkanie1;

public class RemainderOfDividing {
    public static void main(String[] args) {
        int number = 35;
        if(number%7 == 0 && number%5 == 0) {
            System.out.println("Twoja liczba jest podzielna przez 7 oraz 5");
        }else{
        System.out.println("Twoja liczba nie jest podzielna przez 7 oraz 5");
        }
    }
}
